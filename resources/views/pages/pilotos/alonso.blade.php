                <h3>Fernando Alonso</h3>
            </div>
        </div>
        <div class="row">
            <div class="col-sm-6 ">
                Fernando Alonso Díaz (Oviedo, Asturias; 29 de julio de 1981) es un piloto español de automovilismo de velocidad. Ha ganado dos veces el Campeonato Mundial de Pilotos de Fórmula 1 en 2005 y 2006,3​ una vez el Campeonato Mundial de Resistencia de la FIA en 2019, las 24 Horas de Le Mans de 2018 y 2019 y las 24 Horas de Daytona de 2019. También resultó subcampeón de Fórmula 1 en 2010, 2012 y 2013, y obtuvo un tercer puesto en 2007. En Fórmula 1, ha competido para los equipos Minardi, Renault (en el que fue bicampeón del mundo), McLaren y Scuderia Ferrari, siendo esta su escudería desde 2010 hasta 2014. Se marchó de Ferrari y en 2015 volvió a McLaren. Tras dos años fuera de la F1, en la temporada de 2021 vuelve con Alpine F1 Team, parte de la familia Renault, con la que ganó sus 2 campeonatos.
